    /**
     * Returns a safe alternative for emojis on Windows
     * @param emoji The emoji to convert
     * @param fallback The text fallback
     * @return Safe symbol or fallback
     */
    public static String getSafeSymbol(String emoji, String fallback) {
        String os = System.getProperty("os.name").toLowerCase();
        // On older Windows versions, use text fallback
        if (os.contains("win") && !os.contains("windows 10") && !os.contains("windows 11")) {
            return fallback;
        }
        return emoji;
    }
