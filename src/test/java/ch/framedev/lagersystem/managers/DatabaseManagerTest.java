    // =========================================================================
    // inTransaction()
    // =========================================================================

    @Test
    @DisplayName("inTransaction: work block is committed on success")
    void inTransaction_committed_onSuccess() throws Exception {
        // Act
        boolean result = db.inTransaction(conn -> {
            try (var stmt = conn.prepareStatement(
                    "INSERT INTO " + DatabaseManager.TABLE_DEPARTMENTS
                            + " (departmentName, kontoNumber) VALUES (?, ?)")) {
                stmt.setObject(1, "TxDept");
                stmt.setObject(2, "TX1");
                stmt.executeUpdate();
            } catch (Exception e) {
                throw new RuntimeException(e);
            }
        });

        // Assert
        assertTrue(result, "Transaction must commit successfully");
        ResultSet rs = db.executeTrustedQuery(
                "SELECT COUNT(*) FROM " + DatabaseManager.TABLE_DEPARTMENTS
                        + " WHERE departmentName = 'TxDept'");
        assertNotNull(rs);
        try {
            assertTrue(rs.next());
            assertEquals(1, rs.getInt(1));
        } finally {
            db.closeQuery(rs);
        }
    }

    @Test
    @DisplayName("inTransaction: unchecked exception propagates to caller (inTransaction only catches SQLException)")
    void inTransaction_runtimeException_propagatesToCaller() {
        // The Consumer<Connection> interface cannot throw checked exceptions.
        // inTransaction() only catches SQLException, so RuntimeException propagates
        // through and is NOT silently swallowed — the caller receives it.
        assertThrows(RuntimeException.class, () ->
                db.inTransaction(conn -> {
                    try (var stmt = conn.prepareStatement(
                            "INSERT INTO " + DatabaseManager.TABLE_DEPARTMENTS
                                    + " (departmentName, kontoNumber) VALUES (?, ?)")) {
                        stmt.setObject(1, "PropagatedDept");
                        stmt.setObject(2, "RB1");
                        stmt.executeUpdate();
                    } catch (Exception e) {
                        throw new RuntimeException(e);
                    }
                    // Force an unchecked exception to verify propagation behaviour
                    throw new RuntimeException("Simulated failure");
                })
        );
    }

