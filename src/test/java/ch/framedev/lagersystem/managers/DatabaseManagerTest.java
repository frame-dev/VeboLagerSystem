    @Test
    @DisplayName("executeTrustedUpdate: INSERT succeeds and data is queryable")
    void executeTrustedUpdate_insertAndQuery() throws Exception {
        // Arrange
        String insertSql = "INSERT INTO " + DatabaseManager.TABLE_DEPARTMENTS
                + " (departmentName, kontoNumber) VALUES ('TestAbteilung', '9999')";

        // Act
        boolean result = db.executeTrustedUpdate(insertSql);

        // Assert insert succeeded
        assertTrue(result);

        // Verify data was stored
        ResultSet rs = db.executeTrustedQuery(
                "SELECT departmentName FROM " + DatabaseManager.TABLE_DEPARTMENTS
                        + " WHERE departmentName = 'TestAbteilung'");
        assertNotNull(rs);
        try {
            assertTrue(rs.next(), "Expected at least one row");
            assertEquals("TestAbteilung", rs.getString("departmentName"));
        } finally {
            db.closeQuery(rs);
        }
    }

