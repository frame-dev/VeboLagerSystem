    @Test
    @DisplayName("queryOne: returns first row or null when empty")
    void queryOne_returnsFirstRowOrNull() {
        // Empty table → null
        String result = db.queryOne(
                "SELECT departmentName FROM " + DatabaseManager.TABLE_DEPARTMENTS,
                null,
                rs -> {
                    try {
                        return rs.getString("departmentName");
                    } catch (Exception e) {
                        throw new RuntimeException(e);
                    }
                });
        assertNull(result, "Empty table should return null");

        // After insert → returns value
        db.executePreparedUpdate(
                "INSERT INTO " + DatabaseManager.TABLE_DEPARTMENTS + " (departmentName, kontoNumber) VALUES (?,?)",
                new Object[]{"OnlyDept", "999"});

        String found = db.queryOne(
                "SELECT departmentName FROM " + DatabaseManager.TABLE_DEPARTMENTS,
                null,
                rs -> {
                    try {
                        return rs.getString("departmentName");
                    } catch (Exception e) {
                        throw new RuntimeException(e);
                    }
                });
        assertEquals("OnlyDept", found);
    }

