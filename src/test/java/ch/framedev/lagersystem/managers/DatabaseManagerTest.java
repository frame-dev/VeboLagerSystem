    @Test
    @DisplayName("clearDatabase: removes all rows from all application tables")
    void clearDatabase_removesAllData() throws Exception {
        // Arrange: insert data into several tables
        db.executePreparedUpdate(
                "INSERT INTO " + DatabaseManager.TABLE_DEPARTMENTS + " (departmentName, kontoNumber) VALUES (?,?)",
                new Object[]{"D1", "1"});
        db.executePreparedUpdate(
                "INSERT INTO " + DatabaseManager.TABLE_VENDORS
                        + " (name, contactPerson, phoneNumber, email, address, suppliedArticles, minOrderValue) VALUES (?,?,?,?,?,?,?)",
                new Object[]{"Vendor1", "Hans", "0800", "v@v.ch", "Strasse", "A1,A2", 100.0});

        // Act
        boolean result = db.clearDatabase();

        // Assert
        assertTrue(result, "clearDatabase must return true on success");

        ResultSet rs1 = db.executeTrustedQuery(
                "SELECT COUNT(*) FROM " + DatabaseManager.TABLE_DEPARTMENTS);
        assertNotNull(rs1);
        try {
            assertTrue(rs1.next());
            assertEquals(0, rs1.getInt(1), "Departments table must be empty");
        } finally {
            db.closeQuery(rs1);
        }
        ResultSet rs2 = db.executeTrustedQuery(
                "SELECT COUNT(*) FROM " + DatabaseManager.TABLE_VENDORS);
        assertNotNull(rs2);
        try {
            assertTrue(rs2.next());
            assertEquals(0, rs2.getInt(1), "Vendors table must be empty");
        } finally {
            db.closeQuery(rs2);
        }
    }

